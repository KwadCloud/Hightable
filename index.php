      <script>
         $(document).ready(function () {
             var $container = $('.container-sticky');
             var $sticky = $('.sticky');
             var containerOffset = $container.offset().top;
             var containerHeight = $container.outerHeight();
             var stickyHeight = $sticky.outerHeight();
             var stopPoint = containerOffset + containerHeight - stickyHeight;
         
             $(window).on('scroll', function () {
                 var scrollTop = $(window).scrollTop();
         
                 if (scrollTop > containerOffset && scrollTop < stopPoint) {
                     $sticky.css({
                         position: 'fixed',
                         top: 0
                     });
                 } else if (scrollTop >= stopPoint) {
                     $sticky.css({
                         position: 'absolute',
                         top: containerHeight - stickyHeight
                     });
                 } else {
                     $sticky.css({
                         position: 'absolute',
                         top: 0
                     });
                 }
             });
         });
         
